Processor& System::Cpu() { return this->cpu_; }

bool Compare(Process const& x, Process const& y) { return x < y; }

vector<Process>& System::Processes() {

    vector<int> pids = LinuxParser::Pids();
    for (const auto id : pids) {
        Process p;
        p.set_pid(id);
        processes_.push_back(p);
     }
    std::sort(this->processes_.begin(), this->processes_.end(), Compare);

    return this->processes_;
}

